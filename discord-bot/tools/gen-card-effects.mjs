// Effect backfill for the 174 vanilla filler cards (Clay 2026-05-31).
//
// These are systematic vanilla stat-line commons/uncommons (each element
// has c001/c002 1-drops, c009/c010 2-drops, … identical bodies, different
// flavor). The card-visual overhaul wants every card to carry an effect.
//
// Engine-safety first: Boltbound's battle resolver only executes the
// LOCKED keyword set (taunt/charge/shield/stealth/lifesteal/poison/reach/
// spell-immune, see KEYWORDS in cards-content.js). So we assign exactly
// ONE power-budgeted, engine-supported keyword per card, by stat-archetype
// + element theme, and the display text states precisely what the engine
// does (no text that lies about behavior). Deterministic + reproducible.
//
// Voice: terse, mechanical, dry. No em dashes. No fantasy cringe. The
// keyword sentence IS the text.
//
// Output: discord-bot/card-effects-backfill.js exporting GENERATED_EFFECTS.
// cards-content.js merges it into the CARDS map at assembly.
//
// Run:  node tools/gen-card-effects.mjs

import fs from 'fs';

const vanilla = JSON.parse(fs.readFileSync('C:/tmp/vanilla-cards.json', 'utf8'));

// Canonical, engine-accurate text per keyword. Terse + mechanical.
const TEXT = {
  charge:        'Charge. Can attack the turn it is played.',
  taunt:         'Taunt. Enemies must deal with this first.',
  stealth:       'Stealth. Cannot be targeted until it attacks.',
  lifesteal:     'Lifesteal. Damage it deals heals your hero.',
  reach:         'Reach. Can hit the enemy hero through Taunts.',
  poison:        'Poison. Any damage it deals to a minion kills it.',
  shield:        'Shield. Ignores the first damage it takes.',
  'spell-immune':'Spell-Immune. Enemy spells cannot touch it.',
};

// Element prefix → thematic keyword preference (used when the stat line
// doesn't force an archetype). Picked to fit the element's identity
// without overloading any one keyword.
const ELEMENT_KW = {
  shadow: 'stealth', demon: 'stealth',
  fire: 'charge', storm: 'charge', goblin: 'charge', dragon: 'charge',
  frost: 'taunt', forge: 'taunt', vault: 'taunt', undead: 'taunt',
  light: 'lifesteal',
  wild: 'reach', beast: 'reach',
  arcane: 'spell-immune', fae: 'spell-immune',
};

function prefixOf(id) { return id.split('.')[0]; }

// Decide the keyword. Stat archetype wins (it is the clearest read of how
// the card wants to play); element theme is the fallback. Power budget:
// strong evasive keywords (stealth/poison/lifesteal) are capped to small
// bodies so a 7-mana monster never gets stealth.
function pickKeyword(c) {
  const atk = c.atk || 0, hp = c.hp || 0, mana = c.mana || 0;
  const big = mana >= 6 || atk >= 7;

  // Glass cannon (atk >= hp + 2): wants to swing immediately.
  if (atk - hp >= 2) return 'charge';
  // Wall (hp >= atk + 2): wants to soak.
  if (hp - atk >= 2) return 'taunt';

  // Balanced body: element theme, but down-budget risky keywords on
  // large statlines.
  let kw = ELEMENT_KW[prefixOf(c.id)] || 'taunt';
  if (big && (kw === 'stealth' || kw === 'poison' || kw === 'lifesteal')) {
    kw = 'taunt';          // big bodies get the safe defensive keyword
  }
  if (!TEXT[kw]) kw = 'taunt';
  return kw;
}

const out = {};
const tally = {};
for (const c of vanilla) {
  // Tokens are summoned filler; keep the 0-mana ones vanilla-simple but
  // still give them a fitting keyword (charge for the aggressive Bone
  // Knight body, taunt for the defensive Frost Shard).
  const kw = pickKeyword(c);
  out[c.id] = { keywords: [kw], text: TEXT[kw] };
  tally[kw] = (tally[kw] || 0) + 1;
}

const header = `// AUTO-GENERATED by tools/gen-card-effects.mjs (2026-05-31).
// Effect backfill for ${vanilla.length} previously-vanilla filler cards.
// Each entry assigns ONE engine-supported keyword (see KEYWORDS in
// cards-content.js) power-budgeted by stat archetype + element theme, with
// text that states exactly what the resolver does. Merged into the CARDS
// map by cards-content.js. Regenerate by re-running the script.\n\n`;
fs.writeFileSync(
  'card-effects-backfill.js',
  header + 'export const GENERATED_EFFECTS = ' + JSON.stringify(out, null, 2) + ';\n'
);
console.log('wrote card-effects-backfill.js with', Object.keys(out).length, 'entries');
console.log('keyword distribution:', JSON.stringify(tally));
